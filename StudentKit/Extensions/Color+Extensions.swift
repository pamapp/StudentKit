import UIKit

extension UIColor {
    // MARK: - ProgressBar colors -
    @nonobjc class var progressBarGreen: UIColor {
        return UIColor(red: 0.07, green: 0.77, blue: 0.31, alpha: 1.00)
    }
    @nonobjc class var progressBarGray: UIColor {
        return UIColor(red: 0.11, green: 0.11, blue: 0.12, alpha: 1.00)
    }
    
    // MARK: - TableView colors -
    @nonobjc class var cellBackground: UIColor {
        return UIColor(red: 0.90, green: 0.90, blue: 0.90, alpha: 1.00)
    }
}

